"""
Shared state used across the LangGraph workflow.
"""

from typing import List, Optional, TypedDict


class AgentState(TypedDict):
    """
    Shared state passed between every LangGraph node.
    """

    # ==========================================================
    # User Input
    # ==========================================================

    request: str

    # ==========================================================
    # Validation Node
    # ==========================================================

    is_valid: bool
    validation_errors: List[str]

    # ==========================================================
    # Planner Node
    # ==========================================================

    execution_plan: List[str]

    # ==========================================================
    # Decision Node
    # ==========================================================

    document_type: str
    assumptions: List[str]

    # ==========================================================
    # Executor Node
    # ==========================================================

    generated_content: str
    completed_tasks: List[str]

    # ==========================================================
    # Reflection Node
    # ==========================================================

    reflection_feedback: str
    is_approved: bool

    # ==========================================================
    # Document Generation Node
    # ==========================================================

    output_file: str

    # ==========================================================
    # Response Node
    # ==========================================================

    success: bool
    message: str

    # ==========================================================
    # LangGraph Thread / Memory
    # ==========================================================

    thread_id: str
    retry_count: int

    # ==========================================================
    # Error Handling
    # ==========================================================

    error: Optional[str]